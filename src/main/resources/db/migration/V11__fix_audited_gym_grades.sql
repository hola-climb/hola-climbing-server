target_gyms AS (
    SELECT DISTINCT gym.id AS gym_id, gym.website
    FROM gyms gym
    JOIN grade_seed seed ON seed.website = gym.website
    WHERE gym.deleted_at IS NULL
),
deactivated_grades AS (
    UPDATE gym_grades grade
       SET is_active = FALSE,
           updated_at = NOW()
    FROM target_gyms target
    WHERE grade.gym_id = target.gym_id
      AND grade.is_active = TRUE
      AND NOT EXISTS (
          SELECT 1
          FROM grade_seed seed
          WHERE seed.website = target.website
            AND seed.label = grade.label
      )
    RETURNING grade.gym_id, grade.label
),
upserted_grades AS (
    INSERT INTO gym_grades (gym_id, label, difficulty_order, is_active)
    SELECT
        target.gym_id,
        seed.label,
        seed.difficulty_order,
        TRUE
    FROM target_gyms target
    JOIN grade_seed seed ON seed.website = target.website
    ON CONFLICT (gym_id, label) DO UPDATE
       SET difficulty_order = EXCLUDED.difficulty_order,
           is_active = TRUE,
           updated_at = NOW()
    RETURNING gym_id, label
)
SELECT
    (SELECT COUNT(*) FROM target_gyms) AS target_gyms,
    (SELECT COUNT(*) FROM upserted_grades) AS upserted_grades,
    (SELECT COUNT(*) FROM deactivated_grades) AS deactivated_grades;
